let txt = "Estão chegando as provas"; // a contagem começa no 0 

console.log(txt); 
console.log(txt.length); // Quantidade de caracteres no valor da String 
console.log(txt.indexOf("as")); // Retorna a primeira posição na String que tem "as"  
console.log(txt.lastIndexOf("as")); // Retorna a última posição na String que tem "as" 
console.log(txt.slice(0, 5)); // Dividir, pegar um pedaços, ou seja, letras que estão dentro do intervalo de números
console.log(txt.replace("provas", "férias")); // Trocar uma palavra por outra 
console.log(txt.toUpperCase()); // Deixar em maiusculo 
console.log(txt.toLowerCase()); // Deixar em minusculo 

